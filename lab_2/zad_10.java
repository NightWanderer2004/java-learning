public class zad_10 {
   public static void main(String[] args) {
      int a = 10;
      int b = 20;

      System.out.println("a + b = " + (a + b));
      System.out.println("a - b = " + (a - b));
      System.out.println("a * b = " + (a * b));
      System.out.println("b / a = " + (b / a));
      System.out.println("b % a = " + (b % a));

      boolean x = true;
      boolean y = false;

      System.out.println("x && y = " + (x&&y));
      System.out.println("x || y = " + (x||y) );
      System.out.println("!(x && y) = " + !(x && y));
   }
}
