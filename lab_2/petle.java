public class petle {
   public static void main(String[] args) {
      int i = 0;
      // while
      while (i < 10){
         i++;
      }
      
      // do while
      do {
         i++;
      } while (i < 20);

      // for
      for (int j = 0; j < 10; j++) {
         System.out.println(j);
      }
   }
}
